from __future__ import annotations

import tkinter as tk
import webbrowser
from tkinter import ttk
from tkinter.scrolledtext import ScrolledText


def show_help_dialog(app) -> None:
    help_window = tk.Toplevel(app)
    help_window.title("Инструкция")
    help_window.transient(app)
    help_window.grab_set()
    help_window.geometry(f"{int(760 * app.ui_scale)}x{int(620 * app.ui_scale)}")

    outer = ttk.Frame(help_window, style="Card.TFrame", padding=app._pad(18, 18))
    outer.pack(fill="both", expand=True)
    ttk.Label(outer, text="Как пользоваться программой", style="CardTitle.TLabel").pack(anchor="w")

    text = ScrolledText(outer, wrap="word", relief="flat")
    text.pack(fill="both", expand=True, pady=(app._pad_y(12), 0))
    text.insert(
        "end",
        "ПОДКЛЮЧЕНИЕ\n"
        "1. Подключите весы и/или печь к компьютеру.\n"
        "2. Откройте окно «Настройки».\n"
        "3. В блоке «Устройства» выберите COM-порты или нажмите «Автопоиск», который подбирает порты отдельно для весов и печи.\n\n"
        "ЗАПУСК ИЗМЕРЕНИЯ\n"
        "1. Нажмите «Старт».\n"
        "2. График начнет обновляться автоматически.\n"
        "3. Крупные карточки под графиком покажут текущую массу, температуру и статус.\n\n"
        "РАБОТА С ГРАФИКОМ\n"
        "• Справа от графика находятся три панели: «Инструменты», «Виды» и «Анализ».\n"
        "• Пункт «Изображение» в верхнем меню сохраняет график как PNG.\n"
        "• В меню «Экспорт / импорт» можно загрузить сессию, а экспорт CSV и Excel сохраняет таблицу измерений, а не картинку.\n\n"
        "ИНСТРУМЕНТЫ\n"
        "• «Курсор» — показывает время и все видимые линии под мышью: массу, камеру PV и термопару SV.\n"
        "• «Сброс меток» — удаляет все закреплённые метки курсора.\n"
        "• «Вырезать» — убирает выбранный интервал данных по одной оси или сразу по всем. Можно ввести время вручную или сначала отметить диапазон на графике.\n"
        "• «🔍» — включает режим увеличения выбранной области.\n"
        "• «Сдвиг» — перемещает уже увеличенный участок графика.\n"
        "• «+» и «-» — быстро меняют масштаб.\n"
        "• «Авто / сброс» — снимает ручной сдвиг и возвращает удобный автоматический масштаб.\n"
        "• «Время: ...» — переключает подписи оси времени между временем от начала записи и реальными часами.\n"
        "• «Окно: ...» — переключает окно времени между текущим участком и всем графиком от начала записи.\n"
        "• «Кривая: ...» — переключает вид кривой между линиями и точками.\n"
        "• «Сглаж.» — работает отдельно от режима кривой, поэтому точки и линии можно показывать вместе со сглаживанием.\n\n"
        "АНАЛИЗ\n"
        "• «DTG» — показывает скорость изменения массы.\n"
        "• «Нормал.» — переводит массу в относительный масштаб.\n"
        "• «Маркеры» — открывает меню: можно включить маркеры A/B или сразу задать промежуток времени для расчётов.\n"
        "• «Профиль нагрева» — строит эталонную линию нагрева по данным камеры PV.\n"
        "• «Шум» — открывает настройки шумоподавления и позволяет выбрать, к какой линии применять фильтрацию: ко всем, к массе, к камере PV или к термопаре.\n"
        "• Чтобы использовать профиль нагрева: откройте «Анализ», включите кнопку, дождитесь появления чёрной линии и затем сохраняйте PNG, если она нужна в файле.\n"
        "• «Стадии» — помогает выделить этапы процесса.\n"
        "• Вкладка «Отчёт» находится рядом с графиком и таблицей. Там есть расчёты от начальной точки до конца, расчёты по A/B, данные печи, данные весов и кнопка «Обновить данные».\n\n"
        "ЕСЛИ НЕТ COM-ПОРТА\n"
        "• Проверьте кабель и питание устройства.\n"
        "• Установите драйвер USB-Serial или USB-RS485 адаптера.\n"
        "• Убедитесь, что COM-порт не занят другой программой.\n\n"
        "ЕСЛИ УСТРОЙСТВО НЕ ОТВЕЧАЕТ\n"
        "• Проверьте скорость связи и таймаут.\n"
        "• Для печи проверьте slave ID, регистры и линии A/B.\n"
        "• Для весов проверьте режим передачи данных и команду опроса.\n\n"
        "АНАЛИЗ ГРАФИКА\n"
        "• «🔍» — выделение области для детального просмотра.\n"
        "• «Сдвиг» — перемещение уже увеличенного графика.\n"
        "• «+» и «-» — быстрое приближение и отдаление.\n"
        "• «Авто / сброс» — полностью восстановить удобный вид и автоматический масштаб.\n",
    )
    text.configure(state="disabled")
    app._apply_theme_to_toplevel(help_window, text_widget=text)


